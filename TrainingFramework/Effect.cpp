#include "stdafx.h"
#include "Effect.h"
////////////////////////////////////////////////////////////////////////
Effect::Effect(void)
{
}
////////////////////////////////////////////////////////////////////////
Effect::~Effect(void)
{
	SAFE_DEL_ARRAY(m_strName);
	SAFE_DEL_ARRAY(m_pass);
}
////////////////////////////////////////////////////////////////////////
void Effect::SetPassCount(int count)
{
	m_passCount = count;
	m_pass = new Pass[count];
}
////////////////////////////////////////////////////////////////////////
void Effect::Log()
{
	LOGI("Effect: %d, %s, %d", m_id, m_strName, strlen(m_strName));
}
////////////////////////////////////////////////////////////////////////
void Effect::SetName(const char* name)
{
	m_strName = new char[strlen(name)];
	LOGI("SetName: %s %d", name, strlen(name));
	strcpy(m_strName, name);
}
////////////////////////////////////////////////////////////////////////
void Effect::Render()
{
	for (int i = 0; i < m_passCount; i++)
	{
		(m_pass + i)->Render();
	}
}
